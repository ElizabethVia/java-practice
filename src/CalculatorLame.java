
public class CalculatorLame {
	int  plus;
	int minus;
	int multiply;
	int divide;
	
	public  int plus(int n, int nu){
		
	    plus=n+nu;
	   
	  return plus;
	  }
	  public int minus(int m,int min){
		 
	    minus=m-min;
	    return minus;
	  }
	  public int multiply(int mu, int mul){
	    multiply=mu*mul;
	    return multiply;
	  }
	  public int divide(int di, int div){
	    divide =di/div;
	    return divide;
	  }
	
	
	
}
