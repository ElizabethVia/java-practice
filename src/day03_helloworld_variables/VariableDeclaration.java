package day03_helloworld_variables;

public class VariableDeclaration {
	public static void main(String[]args) { 
		//Declare single variable
		int apples;
		//Declare multiple variables in single statement
		int bananas, oranges, mangos;
		
		//declare variable and assing value in single statement
		int kiwi = 14;
		
		System.out.println(kiwi);
		
		//Declare and assign value to multiple variables 
		//in single statement
		
		int watermelon=11, grapes=100, cherries= 12, apricots;
		
		System.out.println(watermelon);
		System.out.println(grapes);
		System.out.println(cherries);
	}

}
